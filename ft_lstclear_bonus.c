#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*tmp;

	if (!lst || !del)
		return ;
	while (*lst)
	{
		tmp = *lst;
		del(tmp->content);
		*lst = (*lst)->next;
		free(tmp);
	}
	lst = NULL;
}
